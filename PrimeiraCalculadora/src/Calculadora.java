public class Calculadora {

    public static void soma (double num1, double num2){
        double resultado = num1 + num2;

        System.out.println("A soma de " +num1+ " com " +num2+ " é: " +resultado);
    }

    public static void sub (double num3, double num4){
        double resultado = num3 - num4;

        System.out.println("A subtração de " +num3+ " com " +num4+ " é: " +resultado);
    }

    public static void div (double num5, double num6){
        double resultado = num5 / num6;

        System.out.println("A divisão de " +num5+ " com " +num6+ " é: " +resultado);
    }

    public static void mult (double num7, double num8){
        double resultado = num7 * num8;

        System.out.println("A multiplicação de " +num7+ " com " +num8+ " é: " +resultado);
    }

}
